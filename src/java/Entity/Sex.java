package Entity;


public class Sex {
    String product_id;
    String sex;

    public Sex() {
    }

    public Sex(String product_id, String sex) {
        this.product_id = product_id;
        this.sex = sex;
    }

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getsex() {
        return sex;
    }

    public void setsex(String sex) {
        this.sex = sex;
    }

  
    
    
    
}
